package avoir.realtime.tcp.launcher;

import java.io.IOException;
import java.util.Properties;
import java.io.FileInputStream;
import java.io.FileOutputStream;
import java.io.File;
import java.io.PrintWriter;
import java.io.FileWriter;

/**
 *
 * @author developer
 */
public class RealtimeOptions {

    static Properties props;
    //user can install the app in any folder..so use the System class to figure out where
    static String fileName = avoir.realtime.tcp.launcher.Constants.getRealtimeHome() + "/conf/realtime.properties";
     static String defaultFileName = avoir.realtime.tcp.launcher.Constants.getRealtimeHome() + "/conf/default.properties";
    static String confDir = avoir.realtime.tcp.launcher.Constants.getRealtimeHome() + "/conf/";
    static String binDir = avoir.realtime.tcp.launcher.Constants.getRealtimeHome() + "/bin/";
    static String libDir = avoir.realtime.tcp.launcher.Constants.getRealtimeHome() + "/lib/";
    static String soundsDir = avoir.realtime.tcp.launcher.Constants.getRealtimeHome() + "/sounds/";
    

    static {
        init();
    }

    private static void write(String filename) {
        write(filename, "DirectConnection=true");
        write(filename, "SystemProxy=false");
        write(filename, "ManualProxy=false");
        write(filename, "ProxyHost=");
        write(filename, "ProxyPort=0");
        write(filename, "UseOnlineSuperNodes=true");
        write(filename, "UseCache=true");
    }

    public static void init() {
        File confFile = new File(confDir);
        File defaultFile = new File(defaultFileName);
        File file = new File(fileName);
        if (!confFile.exists()) {
            confFile.mkdirs();
        }
        if (!defaultFile.exists()) {
            write(defaultFileName);

        }
        if (!file.exists()) {
            write(fileName);
        }
        File binFile = new File(binDir);
        if (!binFile.exists()) {
            binFile.mkdirs();
        }
        File libFile = new File(libDir);
        if (!libFile.exists()) {
            libFile.mkdirs();
        }

        File soundsFile = new File(soundsDir);
        if (!soundsFile.exists()) {
            soundsFile.mkdirs();
        }
        loadProperties();
    }

    private static void write(String filename, String txt) {
        try {
            FileWriter outFile = new FileWriter(filename, true);
            PrintWriter printWriter = new PrintWriter(outFile);
            printWriter.println(txt);
            printWriter.close();
        } catch (IOException e) {
            e.printStackTrace();
        }


    }

    public static void saveProperty(String prop, String value) {
        try {
            FileOutputStream out = new FileOutputStream(fileName);
            props.put(prop, value);
            props.store(out,
                    "---DO NOT EDIT THIS FILE IT IS SYSTEM GENERATED---");
            out.close();
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }

    /**
     * returns true if direct connection is used
     * @return
     */
    public static boolean useDirectConnection() {
        return new Boolean(getProperty("DirectConnection")).booleanValue();
    }

    /**
     * Returns true if system proxy is used
     * @return
     */
    public static boolean useSystemProxy() {
        return new Boolean(getProperty("SystemProxy")).booleanValue();
    }

    public static boolean useCache() {
        String useCache = getProperty("UseCache");
        if (useCache == null) {
            return true;
        }
        if (useCache.equals("")) {
            return true;
        }
        return new Boolean(useCache).booleanValue();
    }

    /**
     * Returns true if manual proxy is used
     * @return
     */
    public static boolean useManualProxy() {
        return new Boolean(getProperty("ManualProxy")).booleanValue();
    }

    /**
     * returns true if configured to use online super nodes
     * @return
     */
    public static boolean useOnlineSuperNodes() {
        return new Boolean(getProperty("UseOnlineSuperNodes")).booleanValue();
    }

    /**
     * 
     * @param status
     */
    public static void setUseOnlineSuperNodes(boolean status) {
        saveProperty("UseOnlineSuperNodes", status + "");
    }

    /**
     * Gets current internet proxy host value
     * @return
     */
    public static String getProxyHost() {
        return getProperty("ProxyHost");
    }

    public static void setProxyHost(String host) {
        saveProperty("ProxyHost", host);
    }

    public static String getProxyPort() {
        return getProperty("ProxyPort");
    }

    public static void setRealtimeProxyPort(String port) {
        saveProperty("RealtimeProxyPort", port);
    }

    public static String getRealtimeProxyPort() {
        return getProperty("RealtimeProxyPort");
    }

    public static void setProxyPort(String port) {
        saveProperty("ProxyPort", port);
    }

    public static String getProperty(String prop) {
        return props.getProperty(prop);
    }

    public static void setUseDirectConnection(boolean use) {
        saveProperty("DirectConnection", use + "");
    }

    public static void setUseManualProxy(boolean use) {
        saveProperty("ManualProxy", use + "");
    }

    public static void setUseSystemProxy(boolean use) {
        saveProperty("SystemProxy", use + "");
    }

    public static void setUseCache(boolean use) {
        saveProperty("UseCache", use + "");
    }

    public static String getConnectionMode() {
        if (useDirectConnection()) {
            return "Direct Connection";
        } else if (useSystemProxy()) {
            return "System Proxy";
        } else {
            return "Manual Proxy Configuration";
        }
    }

    public static void loadProperties() {
        try {

            // create and load default properties
            Properties defaultProps = new Properties();
            FileInputStream in = new FileInputStream(fileName);
            defaultProps.load(in);
            in.close();

            // create program properties with default
            props = new Properties(defaultProps);

            // now load properties from last invocation
            in = new FileInputStream(fileName);
            props.load(in);
            in.close();

        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }
}

