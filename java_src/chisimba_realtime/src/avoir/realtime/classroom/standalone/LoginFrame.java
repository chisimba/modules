    private void connect() {

        String username = nameField.getText();
        if (username.trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Empty names not allowed");
            return;
        }
        boolean presenter = false;
        String host = AppProps.getProperty("SERVER_HOST");
        int port = Integer.parseInt(AppProps.getProperty("SERVER_PORT"));
        new Simulator(username, presenter, host, port, sessionIdField.getText());
        dispose();
    }

